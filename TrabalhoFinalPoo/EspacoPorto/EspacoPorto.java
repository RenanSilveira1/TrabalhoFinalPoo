package EspacoPorto;

public class EspacoPorto{
    int numero;
    String nome;
    double coordX;
    double coordY;
    double coordZ;

    public EspacoPorto(int numero, String nome, double coordX, double coordY, double coordZ){
        this.numero = numero;
        this.nome = nome;
        this.coordX = coordX;
        this.coordY = coordY;
        this.coordZ = coordZ;
    }

    
}